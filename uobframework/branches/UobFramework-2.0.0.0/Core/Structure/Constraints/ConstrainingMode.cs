using System;

namespace UoB.Core.Structure.Constraints
{
	/// <summary>
	/// Summary description for ConstraintMode.
	/// </summary>
	public enum ConstraintMode
	{
		UberAbsolute, // everything is absolute
		CaOAllvAll, // the alpha carbon and backbone oxygen atoms should be constrained
		CaOAllvAllLocalSidechain, // as above, but with the sidechain constrained to its backbone
		AllVsAll,
		CAlpha,
		BackbonePlusBetaCarbon,
		SecondaryStructure,
		BackboneOnly,
        HeavyAtomOnly	
	}
}
